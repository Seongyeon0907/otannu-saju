/**
 * 사주 해석 요약 생성
 */
function buildSummary(enriched: EnrichedSajuResult, bestMonth: number): string {
  const 일간기운 = 오행이름[enriched.일간오행] ?? enriched.일간오행;
  const 강약텍스트 = enriched.강약 === "신강" ? "에너지가 강한" : enriched.강약 === "신약" ? "에너지가 부드러운" : "균형 잡힌";

  return `${일간기운}의 기운을 가진 ${강약텍스트} 사주야. ` +
    `올해 솔로 탈출 가능성이 가장 높은 달은 ${bestMonth}월이야. ` +
    `도화살이 ${enriched.도화살.count}개로 ${enriched.도화살.strength === "없음" ? "은은한 매력을 가지고 있어" : "매력 에너지가 활발한 편이야"}. ` +
    `자신감을 가지고 적극적으로 나서면 좋은 인연이 반드시 찾아올 거야!`;
}

/**
 * 메인: 솔로 탈출 시기 분석 결과 생성
 */
export function calcSoloEscapeResult(enriched: EnrichedSajuResult): SoloEscapeResult {
  const allMonthlyLuck = calcMonthlyLuck(enriched);

  // 현재 달 이후만 포함 (이미 지난 달 제외)
  const currentMonth = new Date().getMonth() + 1;
  const monthlyLuck = allMonthlyLuck.filter((m) => m.month >= currentMonth);

  const bestMonth = monthlyLuck.reduce((best, cur) => cur.score > best.score ? cur : best).month;
  const futurePartner = calcFuturePartner(enriched);
  const summary = buildSummary(enriched, bestMonth);

  return { bestMonth, monthlyLuck, futurePartner, summary };
}
