// ── MBTI 비교 ──

export function getMbtiComparison(mbti: string, totalLoveCount: number): string {
  const mbtiAvg: Record<string, number> = {
    INTJ: 2.8, INTP: 3.1, ENTJ: 4.2, ENTP: 5.1,
    INFJ: 3.3, INFP: 3.7, ENFJ: 4.8, ENFP: 5.5,
    ISTJ: 2.5, ISFJ: 3.0, ESTJ: 3.5, ESFJ: 4.3,
    ISTP: 3.4, ISFP: 3.9, ESTP: 5.8, ESFP: 6.2,
  };

  const avg = mbtiAvg[mbti] ?? 3.5;
  const diff = totalLoveCount - avg;

  if (Math.abs(diff) < 1) {
    return `${mbti}는 평균 ${avg}번 연애한다고 하는데, 너는 ${totalLoveCount}번이라 딱 ${mbti} 평균이야. ${mbti}답게 연애하고 있는 거야. 네 유형의 연애 패턴을 충실히 따르고 있어.`;
  }
  if (diff > 0) {
    return `${mbti}는 평균 ${avg}번 연애한다고 하는데, 너는 ${totalLoveCount}번이라 같은 MBTI 중에서도 연애를 많이 하는 편이야. ${mbti}치고는 사교적인 거야. 사주가 MBTI를 이긴 케이스야.`;
  }
  return `${mbti}는 평균 ${avg}번 연애한다고 하는데, 너는 ${totalLoveCount}번이라 같은 MBTI보다 적어. 근데 걱정 마, 네 사주를 보면 앞으로 남은 연애가 더 진하고 깊을 거야.`;
}

